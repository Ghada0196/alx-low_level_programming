#include "3-calc.h"

/**
 * op_add - return the sum of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: an integer
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference of a and b
 * @a: first number
 * @b: second number
 *
 * Return: an integer
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product of a and b
 * @a: first number
 * @b: second number
 *
 * Return: an integer
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return the result of the division of a by b
 * @a: first number
 * @b: second number
 *
 * Return: an integer
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod -  returns the remainder of the division of a by b
 * @a: first number
 * @b: second number
 *
 * Return: an integer
 */

int op_mod(int a, int b)
{
	return (a % b);
}
